package tech.cae.robotics.sdf.generator.schema;

import com.fasterxml.jackson.dataformat.xml.annotation.JacksonXmlProperty;

/**
 *
 * @author Peter Harman peter.harman@cae.tech
 */
public abstract class AbstractElementOrAttribute extends AbstractSchemaElement {

    @JacksonXmlProperty(localName = "name", isAttribute = true)
    private String name;
    @JacksonXmlProperty(localName = "type", isAttribute = true)
    private String type;
    @JacksonXmlProperty(localName = "default", isAttribute = true)
    private String defaultValue;
    @JacksonXmlProperty(localName = "min", isAttribute = true)
    private String min;
    @JacksonXmlProperty(localName = "max", isAttribute = true)
    private String max;

    public String getName() {
        return name;
    }

    public String getType() {
        return type;
    }

    public String getDefaultValue() {
        return defaultValue;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setType(String type) {
        this.type = type;
    }

    public void setDefaultValue(String defaultValue) {
        this.defaultValue = defaultValue;
    }

    public String getMin() {
        return min;
    }

    public void setMin(String min) {
        this.min = min;
    }

    public String getMax() {
        return max;
    }

    public void setMax(String max) {
        this.max = max;
    }

}
